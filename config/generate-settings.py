#!/usr/bin/env python3
"""Generate Claude Code Lean settings.json and launcher config from profile options."""

from __future__ import annotations

import argparse
import json
import sys
from copy import deepcopy
from pathlib import Path
from typing import Any

CORE_TOOLS = ["Bash", "Read", "Write", "Edit", "WebSearch", "WebFetch"]

TOOL_PACKS: dict[str, list[str]] = {
    "search": ["Glob", "Grep"],
    "tasks": [
        "TodoWrite",
        "TaskCreate",
        "TaskGet",
        "TaskList",
        "TaskOutput",
        "TaskStop",
        "TaskUpdate",
    ],
    "agents": [
        "Agent",
        "EnterPlanMode",
        "ExitPlanMode",
        "EnterWorktree",
        "ExitWorktree",
    ],
    "skills": ["Skill", "ToolSearch"],
    "mcp": ["mcp__*", "ListMcpResourcesTool", "ReadMcpResourceTool", "WaitForMcpServers"],
    "comms": [
        "AskUserQuestion",
        "SendMessage",
        "SendUserMessage",
        "SendUserFile",
        "PushNotification",
    ],
    "extra": [
        "Artifact",
        "Brief",
        "CronCreate",
        "CronDelete",
        "CronList",
        "DesignSync",
        "EndConversation",
        "LSP",
        "Monitor",
        "NotebookEdit",
        "PowerShell",
        "RemoteTrigger",
        "ReportFindings",
        "ScheduleWakeup",
        "ShareOnboardingGuide",
        "Workflow",
    ],
}

ENV_PACK_CLEAR: dict[str, list[str]] = {
    "memory": [
        "CLAUDE_CODE_DISABLE_AUTO_MEMORY",
        "CLAUDE_CODE_DISABLE_ORG_MEMORY",
    ],
    "claude_md": ["CLAUDE_CODE_DISABLE_CLAUDE_MDS"],
    "skills": [
        "CLAUDE_CODE_DISABLE_POLICY_SKILLS",
        "CLAUDE_CODE_DISABLE_CLAUDE_CODE_SKILL",
        "CLAUDE_CODE_DISABLE_CLAUDE_API_SKILL",
    ],
    "agents": [
        "CLAUDE_CODE_DISABLE_EXPLORE_PLAN_AGENTS",
        "CLAUDE_CODE_DISABLE_ADVISOR_TOOL",
    ],
    "thinking": ["CLAUDE_CODE_DISABLE_THINKING"],
    "git": ["CLAUDE_CODE_DISABLE_GIT_INSTRUCTIONS"],
    "cron": ["CLAUDE_CODE_DISABLE_CRON", "CLAUDE_CODE_DISABLE_BACKGROUND_TASKS"],
}

LAUNCHER_TOOL_ORDER = [
    "Bash",
    "Edit",
    "Read",
    "Write",
    "WebFetch",
    "WebSearch",
    "Glob",
    "Grep",
    "TodoWrite",
    "Agent",
    "Skill",
]


def load_base_settings(path: Path) -> dict[str, Any]:
    with path.open(encoding="utf-8") as fh:
        return json.load(fh)


def pack_tools(packs: set[str]) -> set[str]:
    tools: set[str] = set()
    for pack in packs:
        tools.update(TOOL_PACKS.get(pack, []))
    return tools


def build_settings(
    base: dict[str, Any],
    *,
    effort: str,
    packs: set[str],
    hooks: bool,
    thinking_ui: bool,
) -> dict[str, Any]:
    settings = deepcopy(base)
    deny = set(settings["permissions"]["deny"])

    for tool in pack_tools(packs):
        deny.discard(tool)

    settings["env"] = dict(settings.get("env", {}))
    for pack, keys in ENV_PACK_CLEAR.items():
        if pack in packs:
            for key in keys:
                settings["env"].pop(key, None)

    settings["effortLevel"] = effort
    settings["disableAllHooks"] = not hooks
    settings["alwaysThinkingEnabled"] = thinking_ui
    settings["showThinkingSummaries"] = thinking_ui
    settings["autoMemoryEnabled"] = "memory" in packs
    settings["disableBundledSkills"] = "skills" not in packs
    settings["disableWorkflows"] = "agents" not in packs and "extra" not in packs
    settings["disableRemoteControl"] = "extra" not in packs
    settings["disableClaudeAiConnectors"] = "mcp" not in packs
    settings["disableArtifact"] = "extra" not in packs
    settings["disableAgentView"] = "agents" not in packs
    settings["includeGitInstructions"] = "git" in packs
    settings["permissions"]["deny"] = sorted(deny)
    return settings


def enabled_launcher_tools(packs: set[str]) -> list[str]:
    allowed = set(CORE_TOOLS)
    for tool in pack_tools(packs):
        if tool != "mcp__*":
            allowed.add(tool)
    return [tool for tool in LAUNCHER_TOOL_ORDER if tool in allowed]


def build_launcher_config(*, tools: list[str], effort: str, packs: set[str]) -> str:
    disallowed = "" if "mcp" in packs else "mcp__*"
    lines = [
        "# Generated by claude-code-lean installer — do not edit by hand.",
        f'TOOLS="{",".join(tools)}"',
        f'EFFORT="{effort}"',
        f'DISALLOWED="{disallowed}"',
    ]
    return "\n".join(lines) + "\n"


def parse_packs(raw: str) -> set[str]:
    if not raw.strip():
        return set()
    return {part.strip() for part in raw.split(",") if part.strip()}


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--base", type=Path, required=True)
    parser.add_argument("--out-settings", type=Path, required=True)
    parser.add_argument("--out-launcher", type=Path)
    parser.add_argument("--effort", choices=["low", "medium", "high"], default="medium")
    parser.add_argument("--packs", default="", help="Comma-separated pack names")
    parser.add_argument("--hooks", action="store_true")
    parser.add_argument("--thinking-ui", action="store_true")
    parser.add_argument("--launcher", action="store_true", help="Write launcher config")
    args = parser.parse_args()

    packs = parse_packs(args.packs)
    settings = build_settings(
        load_base_settings(args.base),
        effort=args.effort,
        packs=packs,
        hooks=args.hooks,
        thinking_ui=args.thinking_ui,
    )

    args.out_settings.parent.mkdir(parents=True, exist_ok=True)
    with args.out_settings.open("w", encoding="utf-8") as fh:
        json.dump(settings, fh, indent=2)
        fh.write("\n")

    if args.launcher and args.out_launcher:
        args.out_launcher.parent.mkdir(parents=True, exist_ok=True)
        args.out_launcher.write_text(
            build_launcher_config(
                tools=enabled_launcher_tools(packs),
                effort=args.effort,
                packs=packs,
            ),
            encoding="utf-8",
        )

    return 0


if __name__ == "__main__":
    sys.exit(main())
